  void setAccion(String accion) {
    this.accion = accion;
    String titulo = accion + "LIBROS";
    this.setTitle(titulo);
    habilitarControles();
  }

  void setBean(LibrosBean bean) {
    this.bean = bean;
    cargarDatos();
  }

  private void cargarDatos() {
    txtcodigo.setText(bean.getCodigo());
    txtapellido.setText(bean.getApellidos());
    txtnombre.setText(bean.getNombre());
    txtciudad.setText(bean.getCiudad());
    txtobra.setText(bean.getObra());
    txtisbn.setText(bean.getCiudad());
  
  }

  private void habilitarControles() {
    boolean editable = true;
    if(accion.equals(AppUtil.CRUD_ELIMINAR)){
      editable = false;
    }
    txtapellido.setEditable(editable);
    txtnombre.setEditable(editable);
    txtciudad.setEditable(editable);
    txtobra.setEditable(editable);
    txtisbn.setEditable(editable);
   
  }
